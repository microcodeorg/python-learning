"""
变量与类型
"""

number_int = 1  # 整形变量
number_float = 0.9  # 浮点数类型变量
str_1 = "字符串"  # 字符串变量
list_1 = list()  # 列表变量
tuple_1 = tuple()  # 元组变量
dict_1 = dict()  # 字典变量

if __name__ == '__main__':
    # 打印变量名，变量值
    print("number_int", number_int, type(number_int))
    print("number_float", number_float, type(number_float))
    print("str_1", str_1, type(str_1))
    print("list_1", list_1, type(list_1))
    print("tuple_1", tuple_1, type(tuple_1))
    print("dict_1", dict_1, type(dict_1))
